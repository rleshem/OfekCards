package bibcards.line;

public enum LineType {
    Name,       // ש (שם)
    Remark,     // ה (הערה)
    Source,     // מ (מקור)
    HebDate,    // ע (עברי)
    GregDate,   // ל (לועזי)
    Page,       // ד (דף)
    RefKind,    // ס (סוג: מחבר, תרגום, ...)
    Pseudonym,  // ט (שם עט)
    SubTitle,   // ת (תת כותרת)
    Importance, // ח (חשיבות)
    Border      // כ (כרטיס-גנזים)
}
